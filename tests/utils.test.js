const {
    intersect,
    line,
    grid,
} = require("../dist/utils.js");

it("intersect", () => {
    expect(intersect([1, 2], [3, 1, 4])).toBeTruthy();
    expect(intersect([1, 2], [3, ,4])).toBeFalsy();
});

it("line", () => {
  expect(line(4)).toEqual(
    [
          [1], [0, 2], [1, 3], [2]
    ]
  );
});

it("grid", () => {
  expect(grid(3, 4)).toEqual(
    [
          [1, 4], [0, 2, 5], [1, 3, 6], [2, 7],
          [0, 5, 8], [1, 4, 6, 9], [2, 5, 7, 10], [3, 6, 11],
          [4, 9], [5, 8, 10], [6, 9, 11], [7, 10]
    ]
  );
  expect(grid(3, 4, true)).toEqual(
    [
          [1, 4, 5], [0, 2, 4, 5, 6], [1, 3, 5, 6, 7], [2, 6, 7],
          [0, 1, 5, 8, 9], [0, 1, 2, 4, 6, 8, 9, 10], [1, 2, 3, 5, 7, 9, 10, 11], [2, 3, 6, 10, 11],
          [4, 5, 9], [4, 5, 6, 8, 10], [5, 6, 7, 9, 11], [6, 7, 10]
    ]
  );
  expect(grid(5, 5)).toEqual(
      [
          [1, 5], [0, 2, 6], [1, 3, 7], [2, 4, 8], [3, 9],
          [0, 6, 10], [1, 5, 7, 11], [2, 6, 8, 12], [3, 7, 9, 13], [4, 8, 14],
          [5, 11, 15], [6, 10, 12, 16], [7, 11, 13, 17], [8, 12, 14, 18], [9, 13, 19],
          [10, 16, 20], [11, 15, 17, 21], [12, 16, 18, 22], [13, 17, 19, 23], [14, 18, 24],
          [15, 21], [16, 20, 22], [17, 21, 23], [18, 22, 24], [19, 23]  
      ]
  );
});
